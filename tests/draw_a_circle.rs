use rust_katas::draw_a_circle::circle;

#[test]
fn basic_tests() {
    assert_eq!(
        circle(-1),
        "",
        "Negative radii should return the empty string."
    );
    assert_eq!(
        circle(-321),
        "",
        "Negative radii should return the empty string."
    );
    assert_eq!(circle(0), "\n", "A radius of 0 should produce \"\\n\"");
    assert_eq!(circle(1), "█\n");
    assert_eq!(circle(2), "███\n███\n███\n");
    assert_eq!(circle(3), "█████\n█████\n█████\n█████\n█████\n");
    assert_eq!(
        circle(4),
        " █████ \n███████\n███████\n███████\n███████\n███████\n █████ \n"
    );
    assert_eq!(circle(5), "  █████  \n ███████ \n█████████\n█████████\n█████████\n█████████\n█████████\n ███████ \n  █████  \n");
    assert_eq!(circle(6), "  ███████  \n █████████ \n███████████\n███████████\n███████████\n███████████\n███████████\n███████████\n███████████\n █████████ \n  ███████  \n");
    assert_eq!(circle(7), "   ███████   \n  █████████  \n ███████████ \n█████████████\n█████████████\n█████████████\n█████████████\n█████████████\n█████████████\n█████████████\n ███████████ \n  █████████  \n   ███████   \n");
    assert_eq!(circle(8), "    ███████    \n  ███████████  \n █████████████ \n █████████████ \n███████████████\n███████████████\n███████████████\n███████████████\n███████████████\n███████████████\n███████████████\n █████████████ \n █████████████ \n  ███████████  \n    ███████    \n");
    assert_eq!(circle(9), "    █████████    \n   ███████████   \n  █████████████  \n ███████████████ \n█████████████████\n█████████████████\n█████████████████\n█████████████████\n█████████████████\n█████████████████\n█████████████████\n█████████████████\n█████████████████\n ███████████████ \n  █████████████  \n   ███████████   \n    █████████    \n");
    assert_eq!(circle(10), "     █████████     \n    ███████████    \n  ███████████████  \n  ███████████████  \n █████████████████ \n███████████████████\n███████████████████\n███████████████████\n███████████████████\n███████████████████\n███████████████████\n███████████████████\n███████████████████\n███████████████████\n █████████████████ \n  ███████████████  \n  ███████████████  \n    ███████████    \n     █████████     \n");
}
